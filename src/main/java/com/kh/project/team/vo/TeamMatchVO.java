package com.kh.project.team.vo;

public class TeamMatchVO {
	private String matchCode;
	private String homeTeamName;
	private String awayTeamName;
	private int matchResultHometeamScore;
	private int matchResultAwayteamScore;
	private String matchDate;
	private String matchStartTime;
	private String matchEndTime;
	
	public String getMatchCode() {
		return matchCode;
	}
	public void setMatchCode(String matchCode) {
		this.matchCode = matchCode;
	}
	public String getHomeTeamName() {
		return homeTeamName;
	}
	public void setHomeTeamName(String homeTeamName) {
		this.homeTeamName = homeTeamName;
	}
	public String getAwayTeamName() {
		return awayTeamName;
	}
	public void setAwayTeamName(String awayTeamName) {
		this.awayTeamName = awayTeamName;
	}
	public int getMatchResultHometeamScore() {
		return matchResultHometeamScore;
	}
	public void setMatchResultHometeamScore(int matchResultHometeamScore) {
		this.matchResultHometeamScore = matchResultHometeamScore;
	}
	public int getMatchResultAwayteamScore() {
		return matchResultAwayteamScore;
	}
	public void setMatchResultAwayteamScore(int matchResultAwayteamScore) {
		this.matchResultAwayteamScore = matchResultAwayteamScore;
	}
	public String getMatchDate() {
		return matchDate;
	}
	public void setMatchDate(String matchDate) {
		this.matchDate = matchDate;
	}
	public String getMatchStartTime() {
		return matchStartTime;
	}
	public void setMatchStartTime(String matchStartTime) {
		this.matchStartTime = matchStartTime;
	}
	public String getMatchEndTime() {
		return matchEndTime;
	}
	public void setMatchEndTime(String matchEndTime) {
		this.matchEndTime = matchEndTime;
	}
	
	
	
}
