package com.kh.project.menu.vo;

public class MenuVO {
	private String menuCode;
	private String menuName;
	private String menuPath;
	private String menuVideo;
	private String menuEngname;
	
	public String getMenuEngname() {
		return menuEngname;
	}
	public void setMenuEngname(String menuEngname) {
		this.menuEngname = menuEngname;
	}
	public String getMenuVideo() {
		return menuVideo;
	}
	public void setMenuVideo(String menuVideo) {
		this.menuVideo = menuVideo;
	}
	public String getMenuCode() {
		return menuCode;
	}
	public void setMenuCode(String menuCode) {
		this.menuCode = menuCode;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuPath() {
		return menuPath;
	}
	public void setMenuPath(String menuPath) {
		this.menuPath = menuPath;
	}
	
	
}
