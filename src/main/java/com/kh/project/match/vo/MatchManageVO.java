public class MatchManageVO {
	private String matchManageCode;
	private String matchCode;
	private String teamCodeAway;
	private String teamName;
	private String matchManageResponse;
	
	public String getTeamName() {
		return teamName;
	}
	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}
	public String getMatchManageResponse() {
		return matchManageResponse;
	}
	public void setMatchManageResponse(String matchManageResponse) {
		this.matchManageResponse = matchManageResponse;
	}
	public String getMatchManageCode() {
		return matchManageCode;
	}
	public void setMatchManageCode(String matchManageCode) {
		this.matchManageCode = matchManageCode;
	}
	public String getMatchCode() {
		return matchCode;
	}
	public void setMatchCode(String matchCode) {
		this.matchCode = matchCode;
	}
	public String getTeamCodeAway() {
		return teamCodeAway;
	}
	public void setTeamCodeAway(String teamCodeAway) {
		this.teamCodeAway = teamCodeAway;
	}
	
	
	
	
}
