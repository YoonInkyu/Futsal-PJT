package com.kh.project.match.vo;

public class MatchVO {
	private String matchCode;
	private String teamCode;
	private String teamCodeAway;
	private String matchWriter;
	private String matchIntro;
	private String matchRegdate;
	private String matchDate;
	private String matchStartTime;
	private String matchEndTime;
	private String matchPossible;
	private String matchLocation;
	
	public String getMatchCode() {
		return matchCode;
	}
	public void setMatchCode(String matchCode) {
		this.matchCode = matchCode;
	}
	public String getTeamCode() {
		return teamCode;
	}
	public void setTeamCode(String teamCode) {
		this.teamCode = teamCode;
	}
	public String getTeamCodeAway() {
		return teamCodeAway;
	}
	public void setTeamCodeAway(String teamCodeAway) {
		this.teamCodeAway = teamCodeAway;
	}
	public String getMatchWriter() {
		return matchWriter;
	}
	public void setMatchWriter(String matchWriter) {
		this.matchWriter = matchWriter;
	}
	public String getMatchIntro() {
		return matchIntro;
	}
	public void setMatchIntro(String matchIntro) {
		this.matchIntro = matchIntro;
	}
	public String getMatchRegdate() {
		return matchRegdate;
	}
	public void setMatchRegdate(String matchRegdate) {
		this.matchRegdate = matchRegdate;
	}
	public String getMatchDate() {
		return matchDate;
	}
	public void setMatchDate(String matchDate) {
		this.matchDate = matchDate;
	}
	public String getMatchStartTime() {
		return matchStartTime;
	}
	public void setMatchStartTime(String matchStartTime) {
		this.matchStartTime = matchStartTime;
	}
	public String getMatchEndTime() {
		return matchEndTime;
	}
	public void setMatchEndTime(String matchEndTime) {
		this.matchEndTime = matchEndTime;
	}
	public String getMatchPossible() {
		return matchPossible;
	}
	public void setMatchPossible(String matchPossible) {
		this.matchPossible = matchPossible;
	}
	public String getMatchLocation() {
		return matchLocation;
	}
	public void setMatchLocation(String matchLocation) {
		this.matchLocation = matchLocation;
	}
	
	
}
