package com.kh.project.board.vo;

public class FreeImgVO {

	private String freeImgCode;
	private String freeImgOrignName;
	private String freeImgAttachedName;
	private int boardNumFree;

	public String getFreeImgCode() {
		return freeImgCode;
	}

	public void setFreeImgCode(String freeImgCode) {
		this.freeImgCode = freeImgCode;
	}

	public String getFreeImgOrignName() {
		return freeImgOrignName;
	}

	public void setFreeImgOrignName(String freeImgOrignName) {
		this.freeImgOrignName = freeImgOrignName;
	}

	public String getFreeImgAttachedName() {
		return freeImgAttachedName;
	}

	public void setFreeImgAttachedName(String freeImgAttachedName) {
		this.freeImgAttachedName = freeImgAttachedName;
	}

	public int getBoardNumFree() {
		return boardNumFree;
	}

	public void setBoardNumFree(int boardNumFree) {
		this.boardNumFree = boardNumFree;
	}

}
