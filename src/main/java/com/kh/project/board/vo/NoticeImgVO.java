public class NoticeImgVO {

	private String noticeImgCode;
	private String noticeImgOrignName;
	private String noticeImgAttachedName;
	private int boardNumNotice;

	public String getNoticeImgCode() {
		return noticeImgCode;
	}

	public void setNoticeImgCode(String noticeImgCode) {
		this.noticeImgCode = noticeImgCode;
	}

	public String getNoticeImgOrignName() {
		return noticeImgOrignName;
	}

	public void setNoticeImgOrignName(String noticeImgOrignName) {
		this.noticeImgOrignName = noticeImgOrignName;
	}

	public String getNoticeImgAttachedName() {
		return noticeImgAttachedName;
	}

	public void setNoticeImgAttachedName(String noticeImgAttachedName) {
		this.noticeImgAttachedName = noticeImgAttachedName;
	}

	public int getBoardNumNotice() {
		return boardNumNotice;
	}

	public void setBoardNumNotice(int boardNumNotice) {
		this.boardNumNotice = boardNumNotice;
	}

}
